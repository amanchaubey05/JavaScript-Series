// strings in javascript
 let firstname= 'Hemant';
 // length of the string
 console.log(firstname.length);
 // find the particular  by the character help of index
 console.log(firstname[0]);
 
 console.log(firstname[4]);
// to know the last character of the string
 console.log(firstname[firstname.length-1]);

// some important methods of strings
//trim();
// strings are immutable
console.log(firstname.trim());
// toUppercase();
console.log(firstname.toUpperCase());// it capitalize each of the word
// toLowerCase();
console.log(firstname.toLowerCase());// it generally convert all the character in the lower case charcter
// slice method
console.log(firstname.slice(1,5));// it generally returns a substring
